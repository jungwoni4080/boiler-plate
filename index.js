app.post('/api/users/login', (req, res) => {


    //요청된 이메일을 db에서 있는지 찾는다. 
    User.findOne({ email: req.body.email }, (err, user) => {
        if(!user) {
            return res.json({
                loginSuccess: false,
                message: "제공된 이메일을 가진 유저는 없습니다. No email user"
            })
        }
        //요청된 이메일이 db에 있다면, 비밀번호가 맞는 비밀번호인지 확인
        user.comparePassword(req.body.password, (err, isMatch) => {
            if(!isMatch) 
                return res.json({loginSuccess: false, message: "비밀번호가 틀렸습니다. Incorrect PAssword"})
            
            //비밀번호가 맞다면, 토큰을 생성함
            user.generateToken((err, user) => {
            
                if(err) return res.status(400).send(err);
                
                //토큰을 저장한다. 어디에? => 쿠키, 로컬스토리지, 등... => 근데 쿠키에 저장
                    res.cookie("x_auth", user.token)
                    .status(200)
                    .json({ loginSuccess: true,  userId: user._id}) 
            })
        })
    })
})

